import { Download } from 'lucide-react';
import { useEffect, useState } from 'react'

import { QRCodeSVG } from 'qrcode.react';
import style from './DealTicket.module.scss'
import { useAccount } from 'wagmi';

function DealTicket({
	isBuy,
	token,
	tokenName,
	tokenSymbol,
	secondParty,
	price,
	amount,
	buyerSignature,
	sellerSignature,
	buyerPermitSignature,
	sellerPermitSignature,
  themeNonce
}) {
	const account = useAccount();
	const [encoded, setEncoded] = useState('uninitialized');
	const [theme, setTheme] = useState({
		backgroundColor: 'rgb(255, 255, 255)',
		color: 'rgb(0, 0, 0)',
	});

	function randomizeTheme() { // not really
		const colorPair = colors[themeNonce];
		setTheme({
			backgroundColor: `rgb(${colorPair[0].join(',')})`,
			color: `rgb(${colorPair[1].join(',')})`,
		});
	}

	useEffect(() => {
		randomizeTheme();
		setEncoded(JSON.stringify({
			token,
			buyer: isBuy ? account.address : secondParty,
			seller: !isBuy ? account.address : secondParty,
			price, amount,
			buyerSignature,
			sellerSignature,
			buyerPermitSignature,
			sellerPermitSignature,
      
      themeNonce
		}));
	}, [
		isBuy, token,
		account, secondParty,
		price, amount,
		buyerSignature,
		sellerSignature,
		buyerPermitSignature,
		sellerPermitSignature,
    themeNonce
	]);

	return (
		<div className={style.container} style={theme}>
			<div className={style.card}>
				<div className={style.top}>
					<div className={style.qr}>
						<QRCodeSVG value={encoded} bgColor={theme.backgroundColor} fgColor={theme.color} />
					</div>
				
					<div className={style.details}>
						<div className={style.detailsException}>
							<span className={isBuy ? style.buy : style.sell}>{isBuy ? 'BUY' : 'SELL'}</span>
							<span>{tokenName}</span>
						</div>
						<div>
							<span>Price</span>
							<span>{price} USDC</span>
						</div>
						<div>
							<span>Amount</span>
							<span>{amount} {tokenSymbol}</span>
						</div>
					</div>
				</div>
				<div className={style.bottom}>
					<div className={style.details}>
						<div>
							<span>Buyer</span>
							<span>{isBuy ? account.address : secondParty}</span>
						</div>
						<div>
							<span>Seller</span>
							<span>{!isBuy ? account.address : secondParty}</span>
						</div>
						<div>
							<span>Deadline</span>
							<span>20 November 2030 12:30:23 (GMT+1)</span>
						</div>
					</div>
					<div className={style.signDetails}>
						<div>
							<span>Buyer Signed</span>
							<span>{buyerSignature && buyerPermitSignature ? 'Yes' : 'No'}</span>
						</div>
						<div>
							<span>Seller Signed</span>
							<span>{sellerSignature && sellerPermitSignature ? 'Yes' : 'No'}</span>
						</div>
					</div>
				</div>
			</div>
		</div>
	);
}

